/*
 * Servoy Extra Component for embedding YouTube videos.
 * Provides options to configure video dimensions, playback behavior, and display settings.
 */

/**
 * The youtube url can be provided as dataprovider (using dataProviderID property) or as text (using embeddedVideoURL property).
 */
var embeddedVideoURL;

/**
 * The youtube url can be provided as dataprovider (using dataProviderID property) or as text (using embeddedVideoURL property).
 */
var dataProviderID;

/**
 * Specifies the width of the embedded YouTube video.
 */
var videoWidth;

/**
 * Specifies the height of the embedded YouTube video.
 */
var videoHeight;

/**
 * Determines if the video can be viewed in full-screen mode.
 */
var allowFullScreen;

/**
 * Indicates if the video should start playing automatically.
 */
var autoPlay;

/**
 * Controls whether the YouTube player's controls are displayed.
 */
var showControls;

/**
 * Enables a reduced YouTube branding experience.
 */
var modestBranding;

/**
 * Specifies whether related videos are shown after playback ends.
 */
var showRelatedVideosAtEnd;

/**
 * Defines a CSS class for custom styling of the video component.
 */
var styleClass;

/**
 * Specifies the tab order sequence for the video component.
 */
var tabSeq;

/**
 * Determines whether the video component is visible.
 */
var visible;

/**
 * Defines the dimensions of the video component.
 */
var size;

